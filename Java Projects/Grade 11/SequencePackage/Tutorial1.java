public class Tutorial1
{
    public static void main(String []args)
    {
        //Output Syntax
        System.out.print("Nice\n");
        System.out.println("ok");
        System.out.println("ok boomer");
        
        //Terminating characters using \
        // \n --> new line; one enter keypress
        // \t --> indent; one tab keypress
        
        System.out.println("\nA\nB\nC\nD");
        System.out.println("E\tF\tG\tH");
        
        //Output of \ and "" Characters
        System.out.println("I can now use \\ and \"\".");
        
        // To concatenate different values, use +
        System.out.println("Age: " + 20);
        System.out.println('c' + " CVV " + 69);
        System.out.println("Sum: " + 20 + 40);
        System.out.println(20 + 23 + 26);
        System.out.println("Sum: " + (20 + 23));
        
        // Math functions
        // Exponentiation
        System.out.println("Math Pow: " + Math.pow(3, 5));
        System.out.println("Math absolute value: " + Math.abs(-10));
        System.out.println("Math square root: " + Math.sqrt(25));
        
      
        
    }
}